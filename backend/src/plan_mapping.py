"""Конфиг маппинга: строка отчёта -> Code плана (Catalog_ПрибылиУбытки_ПлановыеПоказатели).

Значения Code — строки с ведущими нулями (как в 1С: '000000067').
Маппинг ПОДТВЕРЖДЁН по реально заполненным строкам июльских документов плана
в базе test_moskva (сервисные центры используют показатели 029/067/068/069/070/024;
мойки — 067/074). Часть строк может быть None (производные) — тогда ПЛАН = прочерк.

Все план-показатели заданы как МЕСЯЧНЫЕ; пересчёт в день выполняет
MetricsService (значение месяца ÷ число дней в месяце).
"""
from __future__ import annotations

from typing import Optional

# Строка отчёта -> Code план-показателя (месячное значение).
# None / "" означает «маппинг не подтверждён» -> ПЛАН = None.
PLAN_CODES: dict[str, Optional[str]] = {
    "выручка": "000000067",              # Выручка за месяц
    "себестоимость_зч": "000000070",     # Себестоимость запасных частей
    "выработка_на_мастера": "000000068", # Выработка на 1 мастера план в месяц
    "средний_чек": "000000069",          # Средний чек (для «Закрыто ЗН план»)
    "нормочасы": "000000029",            # Нормочасы (подтверждено по июльским докам)
    "наценка": "000000024",              # Наценка запасные части общая, % (спец. показатель)
    "машин_обслужено": "000000074",      # Машин обслужено (мойки)
    "факт_нормочасы": "000000072",       # Нормочасы за день — общая выработка ФАКТ (1С)
    "факт_выработка_на_мастера": "000000073",  # Выработка на 1 мастера ФАКТ
    # «закрыто ЗН» план = выручка_день / средний_чек — отдельного Code нет
    "закрыто_зн": None,
}


def plan_code(row: str) -> Optional[str]:
    """Code план-показателя для строки отчёта или None, если не задан."""
    code = PLAN_CODES.get(row)
    if not code:
        return None
    return code
