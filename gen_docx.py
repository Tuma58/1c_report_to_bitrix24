def set_cell_bg(cell, hexcolor):
    tcPr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear'); shd.set(qn('w:color'), 'auto'); shd.set(qn('w:fill'), hexcolor)
    tcPr.append(shd)

def set_cell_text(cell, text, bold=False, color=None, size=9.5):
    cell.text = ''
    p = cell.paragraphs[0]
    p.paragraph_format.space_after = Pt(0); p.paragraph_format.space_before = Pt(0)
    run = p.add_run(text)
    run.font.name = 'Arial'; run.font.size = Pt(size); run.bold = bold
    if color is not None: run.font.color.rgb = color

def add_table(headers, rows, widths=None):
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = 'Table Grid'; t.alignment = WD_TABLE_ALIGNMENT.CENTER
    hdr = t.rows[0].cells
    for j, h in enumerate(headers):
        set_cell_text(hdr[j], h, bold=True, color=RGBColor(0xFF, 0xFF, 0xFF)); set_cell_bg(hdr[j], '1F4E79')
    for r in rows:
        cells = t.add_row().cells
        for j, val in enumerate(r):
            set_cell_text(cells[j], val)
    if widths:
        for row in t.rows:
            for j, w in enumerate(widths):
                row.cells[j].width = Inches(w)
    doc.add_paragraph()
    return t

def para(text, color=None, bold=False, italic=False, size=10.5):
    p = doc.add_paragraph()
    r = p.add_run(text)
    r.font.name = 'Arial'; r.font.size = Pt(size); r.bold = bold; r.italic = italic
    if color is not None: r.font.color.rgb = color
    return p

def bullet(text):
    p = doc.add_paragraph(style='List Bullet')
    r = p.add_run(text); r.font.name = 'Arial'; r.font.size = Pt(10.5)
    return p

